void render_rgb_status(void) {
    // abcdefghijklmnopqrstu fits in one line
    oled_write_ln("", false);
    static char tmp[40] = {0};
    static char temp[20] = {0};
    snprintf(tmp, sizeof(tmp) + 1, "%s", rgb_effect_str[rgb_matrix_get_mode()]);
    oled_write_ln(tmp, !rgb_matrix_is_enabled());
    oled_write_ln("", false);
    snprintf(temp, sizeof(temp) + 1, "HUE:  %3d  SAT:  %3d", rgb_matrix_get_hsv().h,  rgb_matrix_get_hsv().s);
    oled_write_ln(temp, false);
    oled_write_ln("", false);
    snprintf(temp, sizeof(temp) + 1, "VAL:  %3d  SPD:  %3d", rgb_matrix_get_hsv().v,  rgb_matrix_get_speed());
    oled_write_ln(temp, false);
}

